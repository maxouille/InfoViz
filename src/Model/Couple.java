package Model;

import java.util.ArrayList;

public class Couple {

	private String genre;
	private int number;
	
	public Couple(String genre, int number) {
		super();
		this.genre = genre;
		this.number = number;
	}

	public String getGenre() {
		return genre;
	}

	public void setGenre(String genre) {
		this.genre = genre;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}
	
	
	

}
